      Local storage v.s. network storage
    </p>
    <p>
      
    </p>
    <p>
      When using network storage there is no dependency on servers. The idea to store the configuration and metadata in MongoDB implies that the client devices must have knowledge about where and how to find any data in order to view the content. This goes also for the searching and filtering of the data and metadata.
    </p>
    <p>
      
